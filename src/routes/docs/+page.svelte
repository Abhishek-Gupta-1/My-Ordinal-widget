<h1><strong> 3. Retrieving Wallet Address:</strong></h1>
<br>
<p>

To retrieve your wallet address, follow these steps:
<br>
<br>
1. Go to the "Wallet" section. <br>
2. Your wallet address will be displayed on this page. This is the address you'll need to fund with BSV Token in order to make transaction(Minting). <br>
